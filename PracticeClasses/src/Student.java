
public class Student {
	
	String name;
	int age = 0;
	int grade = 0;
	

	

	public Student(String studName, int stuAge, int stuGrade) {
		
		this.name = studName;
		this.age = stuAge;
		this.grade = stuGrade;
	}




	public String getName() {
		return name;
	}




	public int getAge() {
		return age;
	}




	public int getGrade() {
		return grade;
	}


	

	


	
	
	
}
